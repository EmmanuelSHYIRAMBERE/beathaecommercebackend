import mongoose from "mongoose";

const contactUsSchema = mongoose.Schema(
  {
    firstname: {
      type: String,
      required: true,
    },
    lastname: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
    isRead: {
      type: Boolean,
      default: false,
    },
  },

  {
    timestamps: true,
  }
);

export const ContactUs = mongoose.model("ContactUs", contactUsSchema);
